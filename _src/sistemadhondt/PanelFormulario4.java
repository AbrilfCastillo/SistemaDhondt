    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        List<Partido> todosLosPartidos = ConexionDAO.mostrarPartidos();
        int totalVotos = todosLosPartidos.stream().mapToInt(Partido::getVotos).sum();
       
        List<Partido> partidos = filtrarPartidos(todosLosPartidos);

        if (partidos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay partidos cargados.");
            return;
        }

        String cargosPrev = ftxtCargos.getText();
        if (cargosPrev.isEmpty() || cargosPrev.equals("0")) {
            JOptionPane.showMessageDialog(null, "No se asign¾ una cantidad de cargos.");
            return;
        }

        int cargos = Integer.parseInt(cargosPrev);

        // Armar cocientes
        List<ResultadoVotos> cocientes = new ArrayList<>();
        for (Partido p : partidos) {
            for (int i = 1; i <= cargos; i++) {
                ResultadoVotos r = new ResultadoVotos(
                    p.getId(),
                    p.getVotos() / i,
                    0,  // orden se asigna despuķs
                    i,
                    (p.getVotos() / (double) totalVotos) * 100,
                    totalVotos
                );
                cocientes.add(r);
            }
        }

        // Ordenar por cociente
        cocientes.sort((a, b) -> Integer.compare(b.getCantidadVotos(), a.getCantidadVotos()));

        // Asignar orden y quedarse solo con los primeros N
        List<ResultadoVotos> resultados = new ArrayList<>();
        for (int i = 0; i < Math.min(cargos, cocientes.size()); i++) {
            ResultadoVotos r = cocientes.get(i);
            r.setOrden(i + 1);
            resultados.add(r);
        }

        // Guardar en la base de datos
        ConexionDAO.insertarResultados(resultados);

        // Navegar al informe
        irAlInforme();
    }//GEN-LAST:event_btnAceptarActionPerformed

