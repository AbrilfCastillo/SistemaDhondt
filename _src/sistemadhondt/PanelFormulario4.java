    private void cargarDatosIniciales(){
        model = new DefaultComboBoxModel();
        for (int i=1; i <= 100; i++){
            model.addElement(i+"%");
        }
        
        cbxPorcentaje.setModel(model);
        ftxtCargos.setText("");
    }
            
    protected List<Partido> filtrarPartidos(List<Partido> partidos){
        int totalVotos = 0;
        
        String seleccion = cbxPorcentaje.getSelectedItem().toString().replace("%", "");
        double porcentaje = Double.parseDouble(seleccion) / 100.0;
        
        for (Partido p : partidos) {
            totalVotos += p.getVotos();
        }
        
        if (totalVotos == 0) return new ArrayList<>();
        
        List<Partido> filtrados = new ArrayList<>();
        
        for (Partido p : partidos) {
            
            double porcentajePartido = p.getVotos() / (double) totalVotos;

            if (porcentajePartido >= porcentaje) {
                filtrados.add(p);
            }
            
            System.out.println("\n=== PORCENTAJES ===");
            System.out.printf("%-15s %8.2f%%%n", p.getNombre(), porcentajePartido * 100);
            
        }
        
        System.out.println("\n=== PARTIDOS QUE PASAN EL FILTRO ===");
        System.out.println(filtrados);
        
        
        return filtrados;
    }
    
    protected void irAlInforme(){
        try {
            java.io.InputStream jrxml = getClass().getResourceAsStream("/informes/report1.jrxml");
            net.sf.jasperreports.engine.JasperReport jasperReport =
                net.sf.jasperreports.engine.JasperCompileManager.compileReport(jrxml);
            java.util.Map<String, Object> parametros = new java.util.HashMap<>();
            
            JasperPrint jasperPrint =
                JasperFillManager.fillReport(
                    jasperReport,
                    parametros,
                    Conexion.conectar()
                );
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException ex) {
            Throwable cause = ex.getCause();
            while (cause != null) {
                System.out.println("Causa: " + cause.getMessage());
                cause = cause.getCause();
            }
            JOptionPane.showMessageDialog(null, "Error al generar el informe: " + ex.getMessage());
        }
    }

