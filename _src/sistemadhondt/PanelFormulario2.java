    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        partidosFiltro = new ArrayList<>();
        
        for (Partido p : partidos) {
            boolean coincideNombre = p.getNombre().toLowerCase().contains(txtFiltro.getText().toLowerCase());
            boolean coincideVotos = String.valueOf(p.getVotos()).contains(txtFiltro.getText());
            if (coincideNombre || coincideVotos) {
                partidosFiltro.add(p);
            }
        }
        
        if(txtFiltro.getText().equals("")){
            filtro = false;
        } else {
            filtro = true;
            indice = 0;
        }

        if (!partidosFiltro.isEmpty()) {
            mostrarActual();
        } else {
            txtNombre.setText("");
            ftxtVotos.setText("");
            lblGuia.setText("0 / 0");
        }
    }//GEN-LAST:event_txtFiltroKeyReleased

