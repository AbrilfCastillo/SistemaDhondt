    private void cargarDatosIniciales() {
        partidos = ConexionDAO.mostrarPartidos();
        indice = 0;

        if (partidos != null && !partidos.isEmpty()) {
            lblMensaje.setText("");
            mostrarActual();
        } else {
            txtNombre.setText("");
            ftxtVotos.setValue(0L);
            lblGuia.setText("0 / 0");
            lblMensaje.setText("No hay partidos registrados");
        }
    }
    
    @Override
    public void setVisible(boolean aFlag) {
        super.setVisible(aFlag);
        if (aFlag) {
            cargarDatosIniciales();
        }
    }
    
    private void mostrarActual() {
        lista = filtro ? partidosFiltro : partidos;

        if (lista == null || lista.isEmpty()) return;

        Partido p = lista.get(indice);
        this.id = p.getId();
        
        this.txtNombre.setText(p.getNombre());
        ftxtVotos.setValue((long) p.getVotos());

        lblGuia.setText((indice + 1) + " / " + lista.size());
    }
    
    
    private void actualizarPartidoActual() {
        lista = filtro ? partidosFiltro : partidos;

        if (lista == null || lista.isEmpty()) return;

        try {
            Partido p = lista.get(indice);
            String nuevoNombre = txtNombre.getText();
            
            ftxtVotos.commitEdit();
            long nuevosVotos = ftxtVotos.getValue() != null ? (Long) ftxtVotos.getValue() : 0L;
            
            if (nuevoNombre.isEmpty()) return;
            
            ConexionDAO.actualizarPartido(p.getId(), nuevoNombre, (int)nuevosVotos);

            
            partidos.set(indice, new Partido(p.getId(), nuevoNombre, (int)nuevosVotos));

        } catch (NumberFormatException e) {
            System.out.println("Error en votos, número no válido.");
            JOptionPane.showMessageDialog(null, "No puede haber casillas vacías.", "", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException e) {
            System.out.println("Error en votos, número no válido.");
            //
        }
    }
    
    private void actualizarRegistro(java.awt.event.KeyEvent evt){
        try {
            if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
                actualizarPartidoActual();
                JOptionPane.showMessageDialog(null,"Actualizado");
            }
            
        } catch (HeadlessException ex){
            System.out.println(ex);
        }
    }

