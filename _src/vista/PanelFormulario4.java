    /**
    * Ejecuta el cálculo D'Hondt con los parámetros ingresados, guarda los
    * resultados en la base de datos y muestra el informe de cargos.
    */
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        List<Partido> todosLosPartidos = PartidoService.obtenerPartidos();
        if (todosLosPartidos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay partidos cargados.");
            return;
        }
        // Suma de votos de todos los partidos, usado como base para los porcentajes
        int totalVotos = todosLosPartidos.stream().mapToInt(Partido::getVotos).sum();
        String porcentaje = cbxPorcentaje.getSelectedItem().toString();
        
        // Filtra los partidos que superan el umbral del porcentaje seleccionado
        List<Partido> partidos = DhondtService.filtrarPartidos(todosLosPartidos, porcentaje);
        if (partidos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay partidos que superen el porcentaje.");
            return;
        }

        String cargosPrev = ftxtCargos.getText();
        if (cargosPrev.isEmpty() || cargosPrev.equals("0")) {
            JOptionPane.showMessageDialog(null, "No se asignó una cantidad de cargos.");
            return;
        }

        int cargos = Integer.parseInt(cargosPrev);
        // Calcula todos los cocientes D'Hondt y los ordena de mayor a menor
        List<ResultadoVotos> cocientes = DhondtService.calcularCocientes(partidos, totalVotos, cargos);
        // Obtiene solo los primeros N cocientes que ocupan cargo
        List<ResultadoVotos> resultados = DhondtService.obtenerResultados(cocientes, cargos);
        
        ResultadoVotosDAO.insertarResultados(resultados);
        // Guarda la tabla completa de división de votos para el segundo informe
        DhondtService.guardarDivisionVotos(cargos, todosLosPartidos, partidos, cocientes, resultados);
        
        irAlInforme();
    }//GEN-LAST:event_btnAceptarActionPerformed
    
