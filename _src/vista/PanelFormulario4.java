    /**
    * Inicializa los controles del panel: carga las opciones de porcentaje
    * del 1% al 100% en el combo box y limpia el campo de cargos.
    */
    private void cargarDatosIniciales(){
        model = new DefaultComboBoxModel();
        for (int i=1; i <= 100; i++){
            model.addElement(i+"%");
        }
        
        cbxPorcentaje.setModel(model);
        ftxtCargos.setText("");
    }
    
    /**
    * Compila y ejecuta el informe de cargos contra la base de datos,
    * luego lo muestra dentro del panel usando mostrarInforme().
    */
    protected void irAlInforme(){
        try {
            // Carga el archivo de diseńo del informe desde el classpath
            java.io.InputStream jrxml = getClass().getResourceAsStream("/informes/informeCargos.jrxml");
            // Compila el .jrxml a un objeto JasperReport ejecutable
            net.sf.jasperreports.engine.JasperReport jasperReport =
                net.sf.jasperreports.engine.JasperCompileManager.compileReport(jrxml);
            java.util.Map<String, Object> parametros = new java.util.HashMap<>();
            
            // Ejecuta el informe contra la base de datos y genera el resultado
            JasperPrint jasperPrint =
                JasperFillManager.fillReport(
                    jasperReport,
                    parametros,
                    Conexion.conectar()
                );
            
            mostrarInforme(jasperPrint);
        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Error al generar el informe: " + e.getMessage());
        }
    }
    
    /**
    * Muestra el informe generado dentro de pnlInforme usando un JRViewer.
    * Agrega un botón "Volver" en la parte superior para regresar a los controles.
    * Oculta pnlCargos y hace visible pnlInforme.
    */
    private void mostrarInforme(JasperPrint jasperPrint){
        JRViewer viewer = new JRViewer(jasperPrint);
        
        // Panel contenedor del botón para limitar su tamańo
        javax.swing.JPanel pnlVolver = new javax.swing.JPanel();
        javax.swing.JButton btnVolver = new javax.swing.JButton("Volver");
        btnVolver.setMaximumSize(new java.awt.Dimension(300, 30));
        btnVolver.setPreferredSize(new java.awt.Dimension(300, 30));
        btnVolver.addActionListener(e -> {
            pnlInforme.setVisible(false); // oculta el informe
            pnlCargos.setVisible(true); // muestra los controles nuevamente
        });
        pnlVolver.add(btnVolver);
        
        pnlInforme.removeAll();
        pnlInforme.setLayout(new BorderLayout());
        pnlInforme.add(pnlVolver, BorderLayout.NORTH);
        pnlInforme.add(viewer, BorderLayout.CENTER);
        pnlInforme.revalidate();
        pnlInforme.repaint();
        
        pnlCargos.setVisible(false); // oculta los controles
        pnlInforme.setVisible(true); // muestra el informe
    }

