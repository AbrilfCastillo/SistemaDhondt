    /**
    * Compila y ejecuta un informe JasperReports desde la ruta indicada,
    * conectándose a la base de datos para obtener los datos, y lo muestra
    * en una ventana separada con el visor de JasperReports.
    * @param ruta ruta del archivo .jrxml dentro del classpath, ej: "/informes/informe.jrxml"
    */
    private void irAlInforme(String ruta){
        try {
            // Carga el archivo de diseńo del informe desde el classpath
            java.io.InputStream jrxml = getClass().getResourceAsStream(ruta);
            // Compila el .jrxml a un objeto JasperReport ejecutable
            net.sf.jasperreports.engine.JasperReport jasperReport =
                net.sf.jasperreports.engine.JasperCompileManager.compileReport(jrxml);
            java.util.Map<String, Object> parametros = new java.util.HashMap<>();
            
            // Ejecuta el informe contra la base de datos y genera el resultado
            JasperPrint jasperPrint =
                JasperFillManager.fillReport(
                    jasperReport,
                    parametros,
                    Conexion.conectar()
                );
            
            // Se encargar de mostrar el informe generado en pantalla
            JRViewer viewer = new JRViewer(jasperPrint);
            pnlPrincipal.removeAll();
            pnlPrincipal.add(viewer);
            pnlPrincipal.revalidate();
            pnlPrincipal.repaint();
            
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Error al generar el informe: " + e.getMessage());
        }
    }
    
