    /**
    * Carga la lista de partidos en el combo box, agregando una opción
    * inicial "(Seleccionar)" como primer elemento para forzar una elección.
    */
    private void cargarLista(){
        model = new DefaultComboBoxModel<>();
        model.addElement(new Partido(0, "(Seleccionar)", 0)); // opción vacía por defecto
        for (Partido p : PartidoService.obtenerPartidos()) {
            model.addElement(p);
        }
        cbxPartidos.setModel(model);
    }
    
    /**
    * Sobrescribe setVisible para recargar la lista de partidos cada vez
    * que el panel se hace visible, reflejando cambios recientes.
    */
    @Override
    public void setVisible(boolean aFlag) {
        super.setVisible(aFlag);
        if (aFlag) {
            cargarLista();
        }
    }

