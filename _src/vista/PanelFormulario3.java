    /**
    * Solicita confirmación y elimina el partido seleccionado en el combo box.
    * Si no hay partido seleccionado, muestra un aviso. Si el usuario confirma
    * la eliminación, borra el registro y recarga la lista.
    */
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // Índice 0 corresponde a la opción "(Seleccionar)"
        if (cbxPartidos.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "No hay un partido seleccionado", "", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String[] opcs = {"Eliminar", "Volver"};
        
        int resp = JOptionPane.showOptionDialog(null,
            "żEstá seguro de eliminar este partido?", "",
            JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null,
            opcs,
            opcs[1]); // "Volver" es la opción por defecto
        
        if (resp == 0) { // el usuario eligió "Eliminar"
            int id = ((Partido) cbxPartidos.getSelectedItem()).getId();
            PartidoService.eliminarPartido(id);
            JOptionPane.showMessageDialog(null,"Eliminado");
            cargarLista(); // recarga el combo box sin el partido eliminado
        }
    }//GEN-LAST:event_btnAceptarActionPerformed


